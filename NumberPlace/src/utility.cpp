#include "pch.h"
#include "stdarg.h"

// -----------------------------------------------------------------------------
// Definitions
// -----------------------------------------------------------------------------

#define DBG_LEN	128	// デバッグ出力バッファの長さ

// -----------------------------------------------------------------------------
// Declarations
// -----------------------------------------------------------------------------

// -----------------------------------------------------------------------------
// Functions
// -----------------------------------------------------------------------------

// @brief デバッグ文字列出力関数です。
// @param format フォーマット文
// @param ... 可変個の引数
void debugprintf(const char* format, ...) {
	va_list ap;
	va_start(ap, format);
	char str[DBG_LEN] = {};
	vsprintf_s(str, format, ap);
	cout << str;
	va_end(ap);
}

